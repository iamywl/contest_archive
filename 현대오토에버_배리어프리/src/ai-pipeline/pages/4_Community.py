# 필터 적용
filtered = CASES
if "전체" not in f_nat and f_nat:
    filtered = [c for c in filtered if c["국적"] in f_nat]
if f_industry:
    filtered = [c for c in filtered if c["산업"] in f_industry]
if f_type:
    filtered = [c for c in filtered if c["유형"] in f_type]
if keyword:
    filtered = [c for c in filtered if keyword in c["본문_ko"] or keyword in c["제목_ko"]]

st.divider()
