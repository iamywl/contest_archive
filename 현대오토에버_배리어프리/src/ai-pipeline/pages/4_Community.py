            chips = (
                f"🌏 **{case['국적']}** · "
                f"🏭 **{case['산업']}** · "
                f"📋 **{case['유형']}** · "
                f"⏱ 처리 **{case['기간']}** · "
                f"💵 **{case['회수액']}**"
            )
            st.caption(chips)

